// When a a tag is clicked with the id "enigme", make the div with the class "secret" visible
// After 5 seconds, the div with the class "secret" is hidden again
// It's a function that is called when it's clicked on the a tag with the id "enigme"
// The function is called "showSecret"
// The function is called "hideSecret"
// The function is called "showSecret" is called when the a tag with the id "enigme" is clicked
// The function is called "hideSecret" is called after 5 seconds

function showSecret() {
    document.querySelector(".secret").style.display = "block";
}

function hideSecret() {
    document.querySelector(".secret").style.display = "none";
}



